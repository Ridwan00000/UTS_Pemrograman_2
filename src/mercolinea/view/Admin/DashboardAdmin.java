    private void btnHapusKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusKategoriActionPerformed
        int row = tblKategori.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kategori terlebih dahulu");
            return;
        }

        int idKategori = Integer.parseInt(
            tblKategori.getValueAt(row, 0).toString()
        );
        int jumlahProduk = Integer.parseInt(
            tblKategori.getValueAt(row, 2).toString()
        );

        if (jumlahProduk > 0) {
            JOptionPane.showMessageDialog(this,
                "Kategori tidak bisa dihapus karena masih digunakan produk!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus kategori ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            controller.hapusKategori(idKategori);
            loadTable();
            JOptionPane.showMessageDialog(this, "Kategori berhasil dihapus");
        }
    }//GEN-LAST:event_btnHapusKategoriActionPerformed

    private void btnEditKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditKategoriActionPerformed
        int row = tblKategori.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pililbl produk terlebih dahulu");
            //            lblNamaAdmin
            return;
        }

        Kategori p = new Kategori();
        p.setIdKategori(Integer.parseInt(tblKategori.getValueAt(row, 0).toString()));
        p.setNamaKategori(tblKategori.getValueAt(row, 1).toString());

        FormKategoriDialog dialog = new FormKategoriDialog(this, true, p);
        dialog.setVisible(true);
        loadTable();
    }//GEN-LAST:event_btnEditKategoriActionPerformed

