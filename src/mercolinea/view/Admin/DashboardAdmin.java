    private void loadTablePembeli() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"No", "ID Pembeli", "Nama Pembeli", "Username", "Total Beli"}, 0
        );
        tblPembeli.setModel(model);

        
        for (Object[] row : controller.getDataPembeli()) {
            model.addRow(row);
        }
    }
    
    private void loadTablePenjual() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"No", "ID Penjual", "Nama Penjual", "Username", "Jumlah Produk", "Kali Dibeli"}, 0
        );
        tblPenjual.setModel(model);

        for (Object[] row : controller.getDataPenjual()) {
            model.addRow(row);
        }
    }
    private void loadTableProduk() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"No", "ID Produk","Nama Produk", "Nama Penjual", "Qty", "Jumlah Terjual"}, 0
        );
        tblProduk.setModel(model);

        for (Object[] row : controller.getDataProduk()) {
            model.addRow(row);
        }
    }



    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblKategori.getModel();
        model.setRowCount(0);

        for (Kategori k : controller.getAll()) {
            model.addRow(new Object[]{
                k.getIdKategori(),
                k.getNamaKategori(),
                k.getJumlahProduk()
            });
        }
    }
    
    private void loadHistory(){
        DefaultTableModel model = new DefaultTableModel(
                new String[]{"Tanggal", "Keterangan", "Nominal"},0
        );
        tblHistory.setModel(model);
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy HH:mm");

        for (SaldoHistory h : chist.getHistoryLogin()) {
            model.addRow(new Object[]{
                sdf.format(h.getTanggal()),
                h.getKeterangan(),
                "Rp " + String.format("%,.0f", h.getNominal())
            });
        }
    }
    private void loadDashboard(){
        lblNamaAdmin.setText("Selamat Datang " + Session.getNama());
        loadTablePenjual();
        loadTablePembeli();
        loadTableProduk();
        loadHistory();
        refreshSaldo();
    }
    public void refreshSaldo() {
        lblKomisiAdmin.setText("Komisi : Rp " + saldoController.getSaldoLogin());
    }
    
    private void logout() {
    // Hapus session
        mercolinea.util.Session.clearSession();

        // Buka form login
        LoginForm login = new LoginForm();
        login.setVisible(true);

        // Tutup dashboard
        this.dispose();
    }

